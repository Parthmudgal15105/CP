void solve()
{
    int n;
    cin >> n;
    // If n is even, it's impossible to guarantee that one number stays at its initial position after n cyclic rotations.
    if (n % 2 == 0)
    {
        cout << -1 << nl; // output -1 and exit if n is even
        return;
    }
    // Construct permutation where each element's offset is exactly 0, 1, ... , n away from its index.
    // For index 0, we assign 0; for the others, we assign n - i.
    for (int i = 0; i < n; i++)
    {
        int val;
        if (i == 0)
        {
            // At index 0, the value is 0 ensuring the correct offset after adding 1.
            val = 0;
        }
        else
        {
            // For indices greater than 0, we compute the value as n - i.
            // This ensures that after cyclic rotation, the element is exactly n away from its initial position.
            val = n - i;
        }
        // Output the computed value + 1 to adjust to the required output format.
        cout << (val + 1) << sp;
    }
    cout << nl;
}

